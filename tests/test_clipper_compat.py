"""
test_clipper_compat.py
======================
Compatibility test suite against a real Clipper 5/Harbour-generated DBF+NTX.

The files are expected at:
    <repo_root>/gera_dbf/PESSOAS.dbf
    <repo_root>/gera_dbf/NOME_IDX.ntx
    <repo_root>/gera_dbf/NASC_IDX.ntx
    <repo_root>/gera_dbf/IDADE_IDX.ntx
    <repo_root>/gera_dbf/CASADO_IDX.ntx

Generated by GERA_DBF.PRG (Harbour/Clipper 5.0) with 1000 records.
The expected values below come directly from resultado_teste.txt produced
by the same Clipper program.

Structure:
    NOME      (C,30)   name
    SOBRENOME (C,40)   surname
    IDADE     (N,3,0)  age  (18..87)
    DT_NASC   (D,8)    birth date
    CASADO    (L,1)    married (T/F)

Indexes:
    NOME_IDX   key = NOME + STR(IDADE,3) + IF(CASADO,"S","N")  [C, 34]
    NASC_IDX   key = DTOS(DT_NASC)                              [C,  8]
    IDADE_IDX  key = STR(IDADE,3)                               [C,  3]
    CASADO_IDX key = IF(CASADO,"S","N")                         [C,  1]

Notes on key encoding
---------------------
All four indexes use CHARACTER keys (the expressions evaluate to strings).
ntx_num_to_str() is NOT used here — the numeric sort order comes from the
fact that STR(N,width) left-pads with spaces, so the string ordering of
" 18" < " 30" < " 65" mirrors numeric ordering.

For CASADO_IDX the keys are 'N' (solteiro) and 'S' (casado).  ASCII
ordering puts 'N' (78) before 'S' (83), matching Clipper's result.
"""

