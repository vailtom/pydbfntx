@pessoas_available
def test_nasc_idx_five_youngest(t):
    """
    Clipper go_bottom + 4 skip(-1) then reversed:
      Sabrina    Martins   26/07/2008
      Cristiano  Esteves   30/07/2008
      Felipe     Fogaca    05/08/2008
      Henrique   Rezende   06/11/2008
      Fernando   Araujo    15/12/2008
    """
    expected_reversed = [
        ("Fernando", "Araujo",  date(2008, 12, 15)),
        ("Henrique", "Rezende", date(2008, 11,  6)),
        ("Felipe",   "Fogaca",  date(2008,  8,  5)),
        ("Cristiano","Esteves", date(2008,  7, 30)),
        ("Sabrina",  "Martins", date(2008,  7, 26)),
    ]
    # Go to last 5 by reading last 5 from iteration (go_bottom not directly usable
    # for "previous 5" without a prev_rec(), so we collect last 5 during iteration)
    t.set_order("nasc_idx")
    tail = []
    for rec in t:
        tail.append((_nome(rec), _sob(rec), rec["DT_NASC"]))
    last5 = tail[-5:]

    # The Clipper output shows them in reverse order (SKIP -1 from bottom),
    # then reverses the array before printing — net result is ascending order.
    # So last5 (ascending) == expected_reversed reversed.
    assert last5 == list(reversed(expected_reversed))


