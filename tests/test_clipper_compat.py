@pessoas_available
def test_nome_idx_sorted_order(t):
    """All records come out in non-decreasing composite-key order."""
    t.set_order("nome_idx")
    prev_key = None
    for rec in t:
        nome = rec["NOME"]  # 30 chars, already padded
        casado_char = "S" if rec["CASADO"] else "N"
        key = nome + f"{rec['IDADE']:3d}" + casado_char
        if prev_key is not None:
            assert key >= prev_key, f"Out of order: {prev_key!r} > {key!r}"
        prev_key = key


